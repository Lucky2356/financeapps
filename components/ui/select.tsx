import { Check, ChevronDown, ChevronUp } from "lucide-react";

import { cn } from "@/lib/utils";

// Themed dropdown built on Radix Select. Replaces native <select> so the OPEN
// list matches the app theme exactly (native lists are OS-rendered: unstylable,
// not rounded, and not reliably dark in WebView2) and the chevron is consistent.
// Pass `name` for form/FormData submission — Radix renders a hidden form input.
//
// NOTE: Radix forbids an empty-string item value. For an "all/any" option use a
// sentinel (e.g. ALL_OPTION) and map it back to "" in the consumer.
export const ALL_OPTION = "__all__";

// FormData helper: Radix submits the sentinel for an "all/any" item, but server
// + filter logic expect an empty string. Run this over a FormData-derived object
// before using it. Mutates a shallow copy.
export function normalizeSelectValues<T extends Record<string, unknown>>(obj: T): T {
  const out = { ...obj };
  for (const key of Object.keys(out)) {
    if (out[key] === ALL_OPTION) (out as Record<string, unknown>)[key] = "";
  }
  return out;
}

